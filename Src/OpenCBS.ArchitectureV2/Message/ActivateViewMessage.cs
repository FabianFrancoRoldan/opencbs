using TinyMessenger;

namespace OpenCBS.ArchitectureV2.Message
{
    public class ActivateViewMessage : ITinyMessage
    {
        public ActivateViewMessage(object sender, object view)
        {
            Sender = sender;
            View = view;
        }
        public object Sender { get; private set; }
        public object View { get; private set; }
    }
}
