using TinyMessenger;

namespace OpenCBS.ArchitectureV2.Message
{
    public class RestartApplicationMessage : ITinyMessage
    {
        public RestartApplicationMessage(object sender, string language)
        {
            Sender = sender;
            Language = language;
        }

        public object Sender { get; private set; }
        public string Language { get; private set; }
    }
}
